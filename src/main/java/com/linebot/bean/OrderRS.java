package com.linebot.bean;

public class OrderRS {

	private String type;
	private String year;
	private Integer seq;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getyear() {
		return year;
	}

	public void setyear(String year) {
		this.year = year;
	}

	public Integer getSeq() {
		return seq;
	}

	public void setSeq(Integer seq) {
		this.seq = seq;
	}

}
